package com.commusoft.diary.diarytrials.TelerikSource;

/**
 * An enumeration with the possible ways to display week numbers
 * in {@link com.telerik.widget.calendar.RadCalendarView}.
 */
public enum WeekNumbersDisplayMode {

    /**
     * The week numbers will not be visualized.
     */
    None,

    /**
     * The week numbers will be presented as part of the first cell of each week.
     */
    Inline,

    /**
     * The week numbers will be presented in a separate cell.
     */
    Block
}
