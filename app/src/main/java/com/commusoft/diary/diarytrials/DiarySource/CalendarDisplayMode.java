package com.commusoft.diary.diarytrials.DiarySource;

/**
 * An enumeration with the different display modes for
 * the {@link com.commusoft.diary.diarytrials.DiarySource.RadCalendarView}.
 */
public enum CalendarDisplayMode {

    /**
     * The calendar view displays one month.
     */
    Month,

    /**
     * The calendar view displays a whole year.
     */
    Year,

    /**
     * The calendar view displays a single week.
     */
    Week
}
